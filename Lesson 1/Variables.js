//In JavaScript, you declare a variable using the var, let, const keywords:

// var: This is the traditional way to declare variables. However, it has some quirks and is now often replaced by let in modern JavaScript.

var name = "John";


// let: Introduced in ES6, it allows you to declare block-scoped local variables. It's now the preferred way to declare variables that will change over time.

let age = 30;


// const: Also introduced in ES6, it's used to declare variables whose values should not be reassigned. It doesn't mean the value it holds is immutable, just that the variable identifier cannot be reassigned.

const PI = 3.14159;